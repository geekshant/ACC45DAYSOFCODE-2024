def is_good(n):
    if n % 4 == 0:
        return "Good"
    else:
        return "Not Good"

def main():
    T = int(input("Enter the number of test cases: "))
    results = []

    for _ in range(T):
        N = int(input())
        results.append(is_good(N))

    print("\nResults:")
    for result in results:
        print(result)

if __name__ == "__main__":
    main()
